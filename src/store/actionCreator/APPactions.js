export function ChangeSize (size){
    return {
        type:'APP_CHANGE_SIZE',
        payload:size
    }
}


export function ChangeIdn (idn){
    return {
        type:'APP_CHANGE_IDN',
        payload:idn
    }
}


export function ChangeColor (color){
    return {
        type:'APP_CHANGE_COLOR',
        payload:color
    }
}